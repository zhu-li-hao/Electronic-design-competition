int main(void)
{
	
	u16 t;
	u8 len;	
	u16 times=0;
//	u8 *coordinate_BUF;
	
  HAL_Init();                    	 	//初始化HAL库    
  Stm32_Clock_Init(RCC_PLL_MUL9);   	//设置时钟,72M
	delay_init(72);               		//初始化延时函数
	usart1_init(115200);					//初始化串口1
	usart3_init(115200);					//初始化串口3
	LED_Init();							//初始化LED	
	KEY_Init();							//初始化按键
    while(1)
    {
			if(USART_RX_STA&0x8000)
		{	
			
			len=USART_RX_STA&0x3fff;//得到此次接收到的数据长度
			
			
//			len=USART_RX_STA&0x3fff;//得到此次接收到的数据长度
			printf("\r\n您发送的消息为:\r\n");
			for(t=0;t<len;t++)
			{
//				coordinate_BUF[t] = USART_RX_BUF[t];
				printf("%c",USART_RX_BUF[t]);
				while(__HAL_UART_GET_FLAG(&UART1_Handler,UART_FLAG_TC)!=SET);		//等待发送结束
			}
			
//			HAL_UART_Transmit(&UART3_Handler,(uint8_t*)USART_RX_BUF,len,1000);	//发送接收到的数据
//			while(__HAL_UART_GET_FLAG(&UART3_Handler,UART_FLAG_TC)!=SET);		//等待发送结束
			printf("\r\n\r\n");//插入换行
			USART_RX_STA=0;
		}else
		{
			times++; 
			if(times%5000==0)
			{
				printf("\r\nALIENTEK 精英STM32开发板 串口实验\r\n");
				printf("正点原子@ALIENTEK\r\n\r\n\r\n");
			}
			if(times%200==0)printf("请输入数据,以回车键结束\r\n");  
			if(times%30==0)LED0=!LED0;//闪烁LED,提示系统正在运行.
			delay_ms(10);   
		} 
    }
}
